package net.mahdilamb.charts.graphics;

/**
 * Enum for markers where joining lines are supported
 */
public enum MarkerMode {
    /**
     * Draw only markers
     */
    MARKER_ONLY,
    /**
     * Draw markers and lines
     */
    MARKER_AND_LINE,
    /**
     * Draw only lines
     */
    LINE_ONLY;
}
